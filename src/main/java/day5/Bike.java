package day5;

public class Bike {
    private String color;
    private String model;
    private int year;

    public String getColor() {
        return color;
    }

    public String getModel() {
        return model;
    }

    public int getYear() {
        return year;
    }

    public Bike(String color, String model, int year) {
        this.color = color;
        this.model = model;
        this.year = year;


    }
}
